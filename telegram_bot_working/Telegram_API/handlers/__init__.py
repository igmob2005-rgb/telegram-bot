from aiogram import Router

# Создаем Главный Роутер. Это наш главный "распределительный щит",
# который принимает ВСЕ входящие запросы от Telegram API и знает, кому их перенаправить.
router = Router()

# --- 🧩 СВЯЗЫВАНИЕ ПРОСТОРНЫХ БЛОКОВ ЛОГИКИ (The Big Components) ---

# 1. Входные точки: Обработка команд / Меню "Назад"
# Мы предполагаем, что в service_entry.py находится роутер для старта и возврата.
from .service_entry import router as entry_router
router.include_router(entry_router)


# 2. Детали заказа: Ядро обработчика всех кликов по кнопкам 'order:'
# Мы предполагаем, что в dispatcher_core.py находится роутер для самого сложного процесса (обработка callback'ов).
from .dispatcher_core import router as core_router
router.include_router(core_router)


# 3. Связывание специализированных модулей:
# Каждый из ваших крупных блоков логики должен иметь свой маленький, отдельный роутер.
# Мы импортируем эти маленькие роутеры и подключаем их к нашему главному щиту.

from .color_routes import router as color_router
router.include_router(color_router)



# Добавляйте сюда все остальные модули, которые вы создадите:
from .input_routes import router as input_router
router.include_router(input_router)


# --- 🎯 ЭКСПОЗИЦИЯ (Exporting) ---
